function assertPayloadShape(name, payload) {
  const required = [
    'id',
    'listing_id',
    'buyer_request_id',
    'contact_name',
    'contact_email',
    'contact_company',
    'contact_phone',
    'inquiry_type',
    'message',
    'status',
  ];
  const forbidden = [
    'listing_slug',
    'listing_title',
    'source_url',
    'name',
    'email',
    'company',
    'country',
    'phone',
    'internal_notes',
  ];
  const allowedInquiryTypes = new Set([
    'quote_routing',
    'listing_verification',
    'listing_submission',
    'wanted_request_submission',
  ]);

  for (const field of required) {
    if (!(field in payload)) throw new Error(`${name} payload missing ${field}`);
  }

  for (const field of forbidden) {
    if (field in payload) throw new Error(`${name} payload includes forbidden field ${field}`);
  }

  if (!allowedInquiryTypes.has(payload.inquiry_type)) {
    throw new Error(`${name} payload has unsupported inquiry_type ${payload.inquiry_type}`);
  }

  if (payload.contact_email !== SMOKE_EMAIL) {
    throw new Error(`${name} payload must use smoke-test email only`);
  }

  if (!payload.message.includes(SMOKE_MARKER)) {
    throw new Error(`${name} payload must include ${SMOKE_MARKER}`);
  }
}

async function restRequest({ url, key, path, method = 'GET', body, prefer }) {
  const response = await fetch(`${url}/rest/v1/${path}`, {
    method,
    headers: makeHeaders(key, prefer),
    body: body ? JSON.stringify(body) : undefined,
  });

  const text = await response.text();
  return { response, text };
}

async function insertInquiry(config, payload) {
  return restRequest({
    url: config.url,
    key: config.anonKey,
    path: 'marketplace_inquiries',
    method: 'POST',
    body: payload,
  });
}

async function assertServiceRoleCanRead(config, id) {
  if (!config.serviceRoleKey) return { skipped: true };

  const { response, text } = await restRequest({
    url: config.url,
    key: config.serviceRoleKey,
    path: `marketplace_inquiries?id=eq.${id}&select=id,status,internal_notes`,
  });

  if (!response.ok) {
    throw new Error(`service role read failed with ${response.status}: ${text}`);
  }

  const rows = JSON.parse(text);
  if (!Array.isArray(rows) || rows.length !== 1 || rows[0].id !== id) {
    throw new Error(`service role read expected inserted row ${id}, got ${text}`);
  }

  return { skipped: false, row: rows[0] };
}

async function closeSmokeRows(config, ids) {
  if (!config.serviceRoleKey || !ids.length) return { skipped: true };

  const idFilter = ids.map((id) => encodeURIComponent(id)).join(',');
  const { response, text } = await restRequest({
    url: config.url,
    key: config.serviceRoleKey,
    path: `marketplace_inquiries?id=in.(${idFilter})`,
    method: 'PATCH',
    body: { status: 'closed', internal_notes: `${SMOKE_MARKER}: closed by smoke cleanup.` },
  });

  return { skipped: false, ok: response.ok, status: response.status, text };
}

